    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        // TODO add your handling code here:
        int id,row ;
        row=jTable3.getSelectedRow();
        if (row<0)
         row+=1;
        id =Integer.parseInt(jTable3.getModel().getValueAt(row, 0).toString());
            try{
      ps=con.prepareStatement("SELECT * FROM Patients WHERE ID=?");
      ps.setInt(1, id);
      rs=ps.executeQuery();
      if(rs.next()){
     ID.setText(rs.getInt("ID")+"");
     fname.setText(rs.getString("First_Name"));
     lname.setText(rs.getString("Last_Name"));
     g=rs.getString("Gender");
     if (g.equals("male"))
          jRadioButton1.setSelected(true);
     else 
         jRadioButton2.setSelected(true);
     phonenum.setText(rs.getInt("Phone_Number")+"");
     Age.setText(rs.getInt("Age")+"");
     }
    }
    catch (SQLException ex) {
        Logger.getLogger(Patient_page.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jTable3MouseClicked

