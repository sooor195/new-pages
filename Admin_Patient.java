    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

        if (ID.getText().isEmpty()||fname.getText().isEmpty()||lname.getText().isEmpty()||phonenum.getText().isEmpty()||Age.getText().isEmpty())
         JOptionPane.showMessageDialog(this,"All fields are required, please make sure to fill it out");
        else{
        id=Integer.parseInt(ID.getText());
        fn=fname.getText();
        Ln=lname.getText();
        phone=Integer.parseInt(phonenum.getText());
        age=Integer.parseInt(Age.getText());
        pass =password.getText();
        try{
            ps=con.prepareStatement("INSERT INTO Patients(ID,First_Name,Last_Name,Phone_Number,Gender,Age) VALUES(?,?,?,?,?,?)");
            ps.setInt(1,id);
            ps.setString(2, fn);
            ps.setString(3, Ln);
            ps.setInt(4, phone);
            ps.setString(5, g);
            ps.setInt(6, age);
             ps.execute();
            ps=con.prepareStatement("INSERT INTO Log_in (ID,Password) VALUES(?,?)");
             ps.setInt(1,id);
             ps.setString(2,pass);
           ps.execute();
             JOptionPane.showMessageDialog(this,"Record added successfully");
           showtable();
             }catch (SQLException ex) {
          Logger.getLogger(Admin_Patient.class.getName()).log(Level.SEVERE, null, ex);
      }}
    }//GEN-LAST:event_addActionPerformed

